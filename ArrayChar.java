public class ArrayChar {
    public static void main(String[] args) {

        char[] chars = {'H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd'};

 
        System.out.println("Characters in the array:");
        for (char ch : chars) {
            System.out.print(ch);
        }
        System.out.println();
    }
}
